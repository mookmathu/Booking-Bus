class SeedApp extends StatelessWidget {
  const SeedApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      home: Scaffold(
        appBar: AppBar(title: const Text('Seed Firebase')),
        body: const _SeedBody(),
      ),
    );
  }
}

class _SeedBody extends StatefulWidget {
  const _SeedBody();

  @override
  State<_SeedBody> createState() => _SeedBodyState();
}

class _SeedBodyState extends State<_SeedBody> {
  String _status = 'กด "อัปโหลด" เพื่อเริ่มต้น';
  bool _loading = false;

  Future<void> _seed() async {
    setState(() {
      _loading = true;
      _status = 'กำลังอัปโหลด...';
    });

    try {
      final db = FirebaseFirestore.instance;
      final config = db.collection('config');

      // 1) อัปโหลด stations
      await config.doc('stations').set({'list': allStations});
      setState(() => _status = '✅ อัปโหลด stations แล้ว\nกำลังอัปโหลด prices...');

      // 2) อัปโหลด price matrix (แบ่งเป็นชุด 20 เพื่อไม่ให้ document ใหญ่เกินไป)
      // Firestore document limit = 1 MB ข้อมูลทั้งหมดประมาณ 400 entries จึงใส่ใน doc เดียวได้
      await config.doc('prices').set(priceMatrix.map(
        (key, value) => MapEntry(key, value),
      ));

      setState(() {
        _status = '✅ เสร็จสมบูรณ์!\n\nอัปโหลดแล้ว:\n• stations: ${allStations.length} สถานี\n• prices: ${priceMatrix.length} เส้นทาง\n\nสามารถลบไฟล์นี้ได้แล้ว';
        _loading = false;
      });
    } catch (e) {
      setState(() {
        _status = '❌ เกิดข้อผิดพลาด:\n$e';
        _loading = false;
      });
    }
  }

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.all(24),
      child: Column(
        mainAxisAlignment: MainAxisAlignment.center,
        children: [
          Text(_status, style: const TextStyle(fontSize: 16)),
          const SizedBox(height: 32),
          if (!_loading)
            ElevatedButton.icon(
              onPressed: _seed,
              icon: const Icon(Icons.cloud_upload),
              label: const Text('อัปโหลดข้อมูลขึ้น Firestore'),
            ),
          if (_loading) const CircularProgressIndicator(),
        ],
      ),
    );
  }
}